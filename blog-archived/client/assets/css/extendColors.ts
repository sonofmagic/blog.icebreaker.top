function withOpacityValue(variable: string) {
  return ({ opacityValue }: { opacityValue?: string }) => {
    if (opacityValue === undefined) {
      return `rgb(var(${variable}))`
    }

    return `rgb(var(${variable}) / ${opacityValue})`
  }
}

